    public function assign(User $user)
    {
        $roles = Role::where("active", 1)->get();
        return view("backend.users.assign", ["roles" => $roles, "member" => $user]);
    }

    public function storeAssign(UserAssignRoleRequest $request, User $user)
    {
        $validated = $request->validated();
        $user->roles()->sync(array_values($validated["roles"]));
        return redirect()->route("admin.users");
